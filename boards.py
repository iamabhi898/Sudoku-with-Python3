def display_brd(brd):
    row_num = 0
    column_num = 0

    for rows in brd:
        for columns in rows:
            print(columns, end=" ")
            column_num += 1
            if column_num == 3 or column_num == 6:
                print("|", end=" ")
        # for new line
        print('')
        row_num += 1
        column_num = 0
        if row_num == 3 or row_num == 6:
            print("---------------------")


